window.MathJax = {
  loader: {
    load: ['[tex]/color'],
    load: ['[tex]/ams']
  },
  tex: {
    inlineMath: [["\\(", "\\)"]],
    displayMath: [["\\[", "\\]"]],
    processEscapes: true,
    processEnvironments: true,
    packages: { '[+]': ['color'] },
    packages: { '[+]': ['ams'] }
  },
  options: {
    ignoreHtmlClass: ".*|",
    processHtmlClass: "arithmatex"
  }
};

